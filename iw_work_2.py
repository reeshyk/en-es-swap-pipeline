def generate_swaps_for_sentence(sentence, doc, translated_doc,
                                 source_lang="en", max_swaps=3):
    """
    Generate swaps at each level 1..max_swaps for a single sentence.
    Each level builds on the previous swap cumulatively.
    Tracks which POS and position each swap was made at.
    Returns dict keyed by n_swaps.
    """
    nlp = nlp_en if source_lang == "en" else nlp_es
    results = {}
    excluded_words = set()
    current_sentence = sentence
    current_doc = doc
    all_swap_details = []

    for n in range(1, max_swaps + 1):
        # Try all POS/position combos in random order
        combos = [(pos, position)
                  for pos in ["NOUN", "ADJ", "VERB"]
                  for position in ["beginning", "middle", "end"]]
        random.shuffle(combos)

        swap_made = False
        for pos, position in combos:
            try:
                result = swap_word_fast(
                    current_sentence, current_doc, translated_doc,
                    source_lang=source_lang, pos=pos, position=position,
                    exclude_words=excluded_words
                )
            except Exception as e:
                print(f"Error in swap_word_fast for sentence '{current_sentence}': {e}")
                result = "no_eligible" # Treat as no eligible to skip this swap

            if isinstance(result, dict):
                excluded_words.add(result["original_word"].lower())
                current_sentence = result["sentence"]
                current_doc = nlp(current_sentence)
                all_swap_details.append({
                    "swap_n": n,
                    "pos": pos,
                    "position": position,
                    "original_word": result["original_word"],
                    "replacement": result["replacement"],
                    "similarity": result["similarity"]
                })
                swap_made = True
                break

        if swap_made:
            results[n] = {
                "sentence": current_sentence,
                "swaps": list(all_swap_details),
                "n_swaps_made": n
            }
        else:
            # Can't make more swaps
            break

    return results


def preprocess_and_generate_multi_swaps(df, source_lang="en", max_swaps=3):
    """
    Pre-translate, pre-parse, then generate swaps at each level
    for every sentence in df.
    """
    if source_lang == "en":
        tokenizer, trans_model = en_es_tokenizer, en_es_model
        source_nlp = nlp_en
        target_nlp = nlp_es
    else:
        tokenizer, trans_model = es_en_tokenizer, es_en_model
        source_nlp = nlp_es
        target_nlp = nlp_en

    texts = df["text"].tolist()
    translations = {}

    print("  Pre-translating...")
    for i in tqdm(range(0, len(texts), 64), desc="Translating"):
        batch = texts[i:i+64]
        inputs = tokenizer(batch, return_tensors="pt", padding=True,
                          truncation=True, max_length=512).to(device)
        with torch.no_grad():
            translated = trans_model.generate(**inputs)
        for src, tgt in zip(batch, translated):
            translations[src] = tokenizer.decode(tgt, skip_special_tokens=True)

    print("  Pre-parsing original sentences...")
    docs = {text: source_nlp(text)
            for text in tqdm(texts, desc="Parsing originals")}

    print("  Pre-parsing translated sentences...")
    translated_docs = {t: target_nlp(t)
                       for t in tqdm(set(translations.values()),
                                     desc="Parsing translations")}

    print("  Generating multi-swaps...")
    all_rows = []
    for idx, row in tqdm(df.iterrows(), total=len(df), desc="Generating swaps"):
        sentence = row["text"]
        doc = docs[sentence]
        translated_sentence = translations.get(sentence)
        translated_doc = translated_docs.get(translated_sentence)

        swap_results = generate_swaps_for_sentence(
            sentence, doc, translated_doc,
            source_lang=source_lang, max_swaps=max_swaps
        )

        row_data = {
            "original_text": sentence,
            "label": row["label"]
        }

        for n in range(1, max_swaps + 1):
            key = f"swap_{n}"
            if n in swap_results:
                row_data[f"{key}_sentence"] = swap_results[n]["sentence"]
                row_data[f"{key}_swaps"] = str(swap_results[n]["swaps"])
                row_data[f"{key}_n_made"] = swap_results[n]["n_swaps_made"]
            else:
                row_data[f"{key}_sentence"] = None
                row_data[f"{key}_swaps"] = None
                row_data[f"{key}_n_made"] = 0

        all_rows.append(row_data)

    return all_rows


