def score_multi_swap_sentiment(rows_data, model_pipeline, model_key, max_swaps=3):
    """Batch score all sentences across all swap levels."""
    all_sentences = []
    for row_data in rows_data:
        all_sentences.append(row_data["original_text"])
        for n in range(1, max_swaps + 1):
            sent = row_data.get(f"swap_{n}_sentence")
            if sent is not None:
                all_sentences.append(sent)

    unique_sentences = list(dict.fromkeys(all_sentences))
    print(f"  Running {model_key} on {len(unique_sentences)} unique sentences...")

    sentiment_results = model_pipeline(
        unique_sentences,
        batch_size=64,
        truncation=True,
        max_length=512
    )

    sentiment_map = {}
    for sentence, result in zip(unique_sentences, sentiment_results):
        label = result["label"]
        if model_key == "mbert":
            numeric = int(label.split()[0])
        elif model_key == "xlm_roberta":
            label_map = {"negative": 1, "neutral": 3, "positive": 5}
            numeric = label_map.get(label.lower(), 3)
        elif model_key == "distilbert":
            label_map = {"negative": 1, "neutral": 3, "positive": 5}
            numeric = label_map.get(label.lower(), 3)
        sentiment_map[sentence] = (numeric, result["score"])

    final_results = []
    for row_data in rows_data:
        current = row_data.copy()
        orig_score, orig_conf = sentiment_map[row_data["original_text"]]
        current["original_score"] = orig_score
        current["original_confidence"] = orig_conf

        for n in range(1, max_swaps + 1):
            key = f"swap_{n}"
            swapped = current.get(f"{key}_sentence")
            if swapped is not None:
                swap_score, swap_conf = sentiment_map[swapped]
                current[f"{key}_score"] = swap_score
                current[f"{key}_confidence"] = swap_conf
                current[f"{key}_score_diff"] = swap_score - orig_score
                current[f"{key}_flipped"] = swap_score != orig_score

        final_results.append(current)

    return pd.DataFrame(final_results)


