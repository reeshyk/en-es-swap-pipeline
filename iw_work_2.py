def get_position(token, doc):
    for sent in doc.sents:
        if token.i >= sent.start and token.i < sent.end:
            sent_length = sent.end - sent.start
            relative_pos = token.i - sent.start
            third = sent_length / 3
            if relative_pos < third:
                return "beginning"
            elif relative_pos < third * 2:
                return "middle"
            else:
                return "end"
    return "middle"

def swap_word_fast(sentence, doc, translated_doc, source_lang="en", pos=None, position=None, exclude_words=None):
    dictionary = en_to_es_dict if source_lang == "en" else es_to_en_dict

    if exclude_words is None:
        exclude_words = set()

    if pos:
        eligible = [token for token in doc if token.pos_ == pos.upper() and token.text.lower() not in exclude_words]
    else:
        eligible = [token for token in doc if token.pos_ in ["NOUN", "ADJ", "VERB"] and token.text.lower() not in exclude_words]

    if position:
        eligible = [token for token in eligible
                    if get_position(token, doc) == position.lower()]

    if not eligible:
        return "no_eligible"

    token = eligible[0]
    lookup = token.lemma_.lower() if token.pos_ == "VERB" else token.text.lower()

    if lookup not in dictionary:
        return "no_candidates"

    translations = dictionary[lookup]

    for target_lemma in translations:
        if target_lemma == lookup:
            continue

        target_lang = "es" if source_lang == "en" else "en"
        target_freq = word_frequency(target_lemma, target_lang)
        source_freq = word_frequency(target_lemma, source_lang)
        if target_freq < source_freq:
            continue

        replacement = get_best_replacement(sentence, token, target_lemma, source_lang)

        context_score = score_in_context(sentence, token.text, replacement)
        if context_score == float("-inf"):
            context_score = score_in_context(sentence, token.text, target_lemma)
            if context_score == float("-inf"):
                continue
            replacement = target_lemma

        is_similar, similarity = verify_semantic_similarity(token.text, replacement)
        if not is_similar:
            continue

        swapped = sentence.replace(token.text, replacement, 1)
        return {
            "sentence": swapped,
            "original_word": token.text,
            "replacement": replacement,
            "similarity": similarity,
            "context_score": context_score
        }

    return "no_candidates"

def _preprocess_and_generate_swaps(df, source_lang="en"):
    """
    Generates all possible swaps and their metadata (excluding sentiment scores).
    This function performs pre-translation, pre-parsing, and swap generation once.
    """
    print(f"Pre-processing and generating swaps for {source_lang.upper()} dataset...")
    all_rows_data = []

    if source_lang == "en":
        tokenizer, trans_model = en_es_tokenizer, en_es_model
        source_nlp = nlp_en
        target_nlp = nlp_es
    else:
        tokenizer, trans_model = es_en_tokenizer, es_en_model
        source_nlp = nlp_es
        target_nlp = nlp_en

    texts = df["text"].tolist()
    translations = {}

    print("  Pre-translating sentences...")
    for i in tqdm(range(0, len(texts), 64), desc="  Translating"):
        batch = texts[i:i+64]
        inputs = tokenizer(batch, return_tensors="pt", padding=True,
                          truncation=True, max_length=512).to(device)
        with torch.no_grad():
            translated = trans_model.generate(**inputs)
        for src, tgt in zip(batch, translated):
            translations[src] = tokenizer.decode(tgt, skip_special_tokens=True)

    print("  Pre-parsing sentences...")
    docs = {text: source_nlp(text) for text in tqdm(texts, desc="  Parsing original sentences")}
    translated_docs = {translated_text: target_nlp(translated_text) for translated_text in tqdm(translations.values(), desc="  Parsing translated sentences")}

    print("  Generating swaps...")
    for idx, row in tqdm(df.iterrows(), total=len(df), desc="  Generating swaps"):
        sentence = row["text"]
        row_data = {
            "original_text": sentence,
            "label": row["label"],
            "no_eligible_count": 0,
            "no_candidates_count": 0
        }

        doc = docs[sentence]
        translated_sentence = translations.get(sentence)
        translated_doc = translated_docs.get(translated_sentence) if translated_sentence else None

        for pos in ["NOUN", "ADJ", "VERB"]:
            for position in ["beginning", "middle", "end"]:
                swap_result = swap_word_fast(
                    sentence, doc, translated_doc,
                    source_lang=source_lang, pos=pos, position=position
                )
                key = f"{pos}_{position}"
                if swap_result == "no_eligible":
                    row_data["no_eligible_count"] += 1
                    row_data[f"{key}_sentence"] = None
                elif swap_result == "no_candidates":
                    row_data["no_candidates_count"] += 1
                    row_data[f"{key}_sentence"] = None
                else:
                    row_data[f"{key}_sentence"] = swap_result["sentence"]
                    row_data[f"{key}_original_word"] = swap_result["original_word"]
                    row_data[f"{key}_replacement"] = swap_result["replacement"]
                    row_data[f"{key}_similarity"] = swap_result["similarity"]

        all_rows_data.append(row_data)
    return all_rows_data

def analyze_pregenerated_swaps_with_sentiment(pregenerated_rows_data, model_pipeline, model_key):
    """
    Takes pre-generated swap data and evaluates sentiment using the given model.
    """
    print(f"Collecting sentences for {model_key} sentiment analysis...")
    all_sentences_for_sentiment = []
    for row_data in pregenerated_rows_data:
        all_sentences_for_sentiment.append(row_data["original_text"])
        for pos in ["NOUN", "ADJ", "VERB"]:
            for position in ["beginning", "middle", "end"]:
                key = f"{pos}_{position}"
                if row_data.get(f"{key}_sentence") is not None:
                    all_sentences_for_sentiment.append(row_data[f"{key}_sentence"])

    unique_sentences = list(dict.fromkeys(all_sentences_for_sentiment))
    print(f"Running {model_key} sentiment on {len(unique_sentences)} unique sentences...")

    sentiment_results = model_pipeline(
        unique_sentences,
        batch_size=64,
        truncation=True,
        max_length=512
    )

    sentiment_map = {}
    for sentence, result in zip(unique_sentences, sentiment_results):
        label = result["label"]
        score = result["score"]
        if model_key == "mbert":
            numeric = int(label.split()[0])
        elif model_key == "xlm_roberta":
            label_map = {"negative": 1, "neutral": 3, "positive": 5}
            numeric = label_map.get(label.lower(), 3)
        elif model_key == "distilbert":
            label_map = {"negative": 1, "positive": 5}
            numeric = label_map.get(label.lower(), 3)
        sentiment_map[sentence] = (numeric, score)

    print("Assembling final results with sentiment scores...")
    final_results = []
    for row_data in pregenerated_rows_data:
        current_row_data = row_data.copy() # Make a copy to add sentiment scores
        orig_score, orig_conf = sentiment_map[current_row_data["original_text"]]
        current_row_data["original_score"] = orig_score
        current_row_data["original_confidence"] = orig_conf

        for pos in ["NOUN", "ADJ", "VERB"]:
            for position in ["beginning", "middle", "end"]:
                key = f"{pos}_{position}"
                swapped_sentence = current_row_data.get(f"{key}_sentence")
                if swapped_sentence is not None:
                    swap_score, swap_conf = sentiment_map[swapped_sentence]
                    current_row_data[f"{key}_score"] = swap_score
                    current_row_data[f"{key}_confidence"] = swap_conf
                    current_row_data[f"{key}_score_diff"] = swap_score - orig_score

        all_results.append(current_row_data)

    return pd.DataFrame(final_results)


